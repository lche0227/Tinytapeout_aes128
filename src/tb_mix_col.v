// =============================================================================
// tb_mix_col.v – Testbench for AES MixColumns
// =============================================================================
// Tests MixColumns on full 128-bit state
// =============================================================================

`timescale 1ns / 1ps

module tb_mix_col;

    // -------------------------------------------------------------------------
    // Inputs / Outputs
    // -------------------------------------------------------------------------
    reg  [127:0] data_in;
    wire [127:0] data_out;

    // -------------------------------------------------------------------------
    // DUT
    // -------------------------------------------------------------------------
    mix_col uut (
        .data_in  (data_in),
        .data_out (data_out)
    );

    // -------------------------------------------------------------------------
    // Testbench
    // -------------------------------------------------------------------------
    initial begin

        $display("==============================================");
        $display(" AES MixColumns Testbench");
        $display("==============================================");

        // ==============================================================
        // TEST 1: Example
		  // ==============================================================
        //
        // Input:
			//[ 12 25 c1 3f ]
			//[ c9 da 5f 8e ] 
			//[ 94 de 00 44 ]
			//[ a8 75 5f 2c ]

         // Expected output after MixColumns:
			//[ 58 94 27 9f ]
			//[ 94 86 20 d8 ]
			//[ 0b c7 7f 4d ]
			//[ 20 81 b9 d3 ]
			
        data_in = 128'h12c994a825dade75c15f005f3f8e442c;
        #10;

        $display("\nTEST 1");
        $display("Input  = %h", data_in);
        $display("Output = %h", data_out);

        if (data_out == 128'h58940b209486c78127207fb99fd84dd3)
            $display("PASS");
        else
            $display("FAIL");

        // ==============================================================
        // TEST 2:https://www.youtube.com/watch?v=ad_by3uiryk
        // ==============================================================
		  //
        // Input:
			//[ 87 f2 4d 97 ]
			//[ 6e 4c 90 ec ] 
			//[ 46 e7 4a c3 ]
			//[ a6 8c d8 95 ]

        // Expected output after MixColumns:
			//[ 47 40 a3 4c ]
			//[ 37 d4 e4 a5 ]
			//[ a3 70 3a a6 ]
			//[ 4c 9f 42 bc ]
        data_in = 128'h876e46a6f24ce78c4d904ad897ecc395;
        #10;

        $display("\nTEST 2");
        $display("Input  = %h", data_in);
        $display("Output = %h", data_out);

        if (data_out == 128'h473794ed40d4e4a5a3703aa64c9f42bc)
            $display("PASS");
        else
            $display("FAIL");

        // ==============================================================
        // TEST 3: All zeros
        // ==============================================================

        data_in = 128'h00000000000000000000000000000000;
        #10;

        $display("\nTEST 3");
        $display("Input  = %h", data_in);
        $display("Output = %h", data_out);

        if (data_out == 128'h00000000000000000000000000000000)
            $display("PASS");
        else
            $display("FAIL");

        // ==============================================================
        // Finish simulation
        // ==============================================================

        $display("\n==============================================");
        $display(" Simulation Finished");
        $display("==============================================");

        $stop;
    end

endmodule